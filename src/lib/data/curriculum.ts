// ──────────────────────────────────────
// 헬퍼: Step 생성 유틸
// ──────────────────────────────────────

function step(
  currId: string,
  day: number,
  order: number,
  instruction: string,
  duration: number,
  tips: string[],
  altB?: { title: string; description: string },
  altC?: { title: string; description: string },
): TrainingStep {
  return {
    id: `${currId}_d${day}_s${order}`,
    order,
    instruction,
    duration_minutes: duration,
    tips,
    variants: {
      A: { instruction_override: null, duration_override: null, difficulty_note: '표준 방법' },
      B: altB
        ? { instruction_override: altB.description, duration_override: null, difficulty_note: altB.title }
        : { instruction_override: null, duration_override: null, difficulty_note: '표준 방법과 동일' },
      C: altC
        ? { instruction_override: altC.description, duration_override: null, difficulty_note: altC.title }
        : { instruction_override: null, duration_override: null, difficulty_note: '표준 방법과 동일' },
    },
  };
}

