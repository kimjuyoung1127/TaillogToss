/** 전체 커리큘럼 목록 (7종) */
export const CURRICULUMS: Curriculum[] = [
  basicObedience,
  leashManners,
  separationAnxiety,
  reactivityManagement,
  impulseControl,
  socialization,
  fearDesensitization,
];

/** ID로 커리큘럼 조회 */
export function getCurriculumById(id: CurriculumId): Curriculum | undefined {
  return CURRICULUMS.find((c) => c.id === id);
}

/** BehaviorType → 추천 커리큘럼 반환 */
export function getRecommendedCurriculum(behaviors: BehaviorType[]): Curriculum {
  for (const behavior of behaviors) {
    const id = BEHAVIOR_TO_CURRICULUM[behavior];
    if (id) {
      const curriculum = getCurriculumById(id);
      if (curriculum) return curriculum;
    }
  }
  return basicObedience; // 폴백: 기본 커리큘럼
}

